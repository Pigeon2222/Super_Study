#include<stdio.h>

int main() {
	printf("내 이름은 %s입니다.\n", "이동준");
	printf("내 혈액형은 %s입니다.\n", "AB");
	printf("내 나이는 %d살이고, 나는 %d년생입니다.\n", 62, 1961);
	printf("내 MBTI는 %s이고, 내 키는 %f입니다.\n", "ISTP",173.0);
	printf("내 별명은 \"%s\"입니다.", "준동이");
	printf("내 별명은 \"%s\"입니다.", "430");
	printf("내 별명은 \"%c\"입니다.", '7');
	printf("내 별명은 \"%d\"입니다.", '7');

	/*
	컨트롤+d : 줄 복사
	컨트롤+x : 줄 잘라내기
	알트+시프트 : 여러자리 선택
	알트+컨트롤 : 마우스클릭 여러자리 선택
	*/
	
	return 0;
}